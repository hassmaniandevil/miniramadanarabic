// Combine all stories
export const allBedtimeStories: BedtimeStory[] = [
  ...littleStarStories,
  ...explorerStories,
  ...grownUpStories,
];

// Helper functions
export function getStoryForDay(day: number, ageGroup: AgeGroup): BedtimeStory | undefined {
  return allBedtimeStories.find(
    (story) => story.day === day && story.ageGroup === ageGroup
  );
}

export function getStoriesForAgeGroup(ageGroup: AgeGroup): BedtimeStory[] {
  return allBedtimeStories.filter((story) => story.ageGroup === ageGroup);
}

export function getAgeGroupLabel(ageGroup: AgeGroup): string {
  switch (ageGroup) {
    case 'little_star':
      return 'Little Star (Ages 3-6)';
    case 'explorer':
      return 'Explorer (Ages 7-9)';
    case 'grown_up':
      return 'Grown-Up Reader (Ages 10-12)';
    default:
      return 'Unknown';
  }
}

export function getAgeGroupFromProfileType(profileType: string): AgeGroup {
  switch (profileType) {
    case 'little_star':
      return 'little_star';
    case 'child':
      return 'explorer';
    case 'adult':
      return 'grown_up';
    default:
      return 'explorer';
  }
}
